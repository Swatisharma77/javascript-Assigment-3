// Given equation: y = 2x - 2

// Calculate the slope (m), which is the coefficient of x
const slope = 2;

// Calculate the y-intercept (b), which is the constant term
const yIntercept = -2;

// Calculate the x-intercept, which is the point where y = 0
const xIntercept = -2 / 2; // x-intercept is -1

// Display the results
console.log("Slope (m) =", slope);
console.log("Y-Intercept (b) =", yIntercept);
console.log("X-Intercept =", xIntercept);

