// Define the coordinates of the two points
const x1 = 2;
const y1 = 2;
const x2 = 6;
const y2 = 10;

// Calculate the slope (m) using the formula
const slope = (y2 - y1) / (x2 - x1);

// Display the calculated slope
console.log(`The slope between (${x1}, ${y1}) and (${x2}, ${y2}) is: ${slope}`);
